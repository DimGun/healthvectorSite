	<p>
		 Аутизм (детский аутизм) - расстройство, возникающее вследствие нарушения развития головного мозга. Вызывает отклонения в поведении, общении и социальном взаимодействии. Это пожизненное состояние. Причиной возникновения аутизма, предположительно, является сочетание токсических воздействий на организм во время беременности и родов&nbsp; (различные осложнения, инфекционные заболевания),&nbsp;а также генетические факторы.&nbsp;
