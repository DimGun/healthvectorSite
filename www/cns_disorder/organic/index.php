			<p>
				 Савантизм<b> </b>– это явление, когда ребенок-аутист очень одарен в одной определенной области. Некоторые аутисты способны сыграть мелодию с одного прослушивания или вычислять в уме сложнейшие примеры. Знаменитые аутисты мира – Альберт Эйнштейн, Вуди Аллен, Энди Уорхол.
