				 Травля с применением физической агрессии по отношению к детям с особенностями развития довольно часто встречается в средних и старших классах школы. Это могут быть пинки, удары или попытки толкнуть, подставить подножку и бросить каким-то предметом в классе. Заранее научите ребенка тому, что делать в таком случае.
