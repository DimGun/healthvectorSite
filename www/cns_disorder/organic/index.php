 <br>
		<p>
			 Аутизм может проявляться в поведении детей в возрасте 1,5–2 лет и старше. Социальное взаимодействие ребенка с другими детьми и взрослыми нарушено. Это ведет к изоляции малыша от семьи, общества. При аутизме отмечается максимальный дефицит эмоций и сферы общения. Ребенок находится&nbsp;«внутри себя» и не нуждается в окружающих. Его жесты и речь не направляются во внешний мир. Диагноз наиболее часто ставят детям в возрасте 3-5 лет и называют его РДА или синдром Каннера.
